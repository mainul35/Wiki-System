    private void btnSignUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSignUpActionPerformed
        // TODO add your handling code here:
        String uname = txtUserName.getText();
        String password = txtPassword.getText();
        if(uname.isEmpty()){
            txtUserName.setBackground(Color.decode("#ffbbbb"));
        }else if(password.isEmpty()){
            txtUserName.setBackground(Color.decode("#ffffff"));
            txtPassword.setBackground(Color.decode("#ffbbbb"));
        }else{
            
            try {
                MiddleWare.createAdmin(uname, password);
                CreateUserInformation.createDirectory("Admin\\"+uname);
            } catch (SQLException ex) {
                ex.printStackTrace();
                Logger.getLogger(AdminSignupWindow.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnSignUpActionPerformed

